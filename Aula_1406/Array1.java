package Aula_1406;

public class Array1 {

	public static void main(String[] args) {
		int[] idade = {10,16,27,34,80,90};
		int vassoura;
        System.out.println("varrendo as idades...");
        for(vassoura = 0; vassoura < 6; vassoura++) {
        	System.out.println("sua idade e:\n"+ idade[vassoura]);
        }//fim for
	}//fim main
}//fim classe
