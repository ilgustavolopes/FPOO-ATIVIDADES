package Aula_1206;

public class IfElse {

	public static void main(String[] args) {
		int numero=16;
		
		if(numero >= 16 && numero <= 18) {
			System.out.println("Voce pode votar");
	    }
		else {
			System.out.println("Voce nao pode votar");
		}
	}
}
